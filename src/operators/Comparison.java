package operators;

public class Comparison {

	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;
		
		if(value1 == value2) {
			System.out.println("Values are equal");
		}
		
		if(value1 != value2) {
			System.out.println("values are not equal");
		}
		
		if(value1 > value2) {
			System.out.println("value1 is greater then value2");
		}
		
		if(value1 < value2) {
			System.out.println("value1 is less then value2");
		}
		
		if(value1 <= value2) {
			System.out.println("value1 is less then equal to value2");
		}

	}

}
